const mongoose = require('mongoose');

const fileSchema = new mongoose.Schema({
  user: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'User',
    required: true
  },
  filename: {
    type: String,
    required: true
  },
  path: {
    type: String,
    required: true
  },
  mimetype: {
    type: String,
    required: true
  },
  size: {
    type: Number,
    required: true
  },
  uploadDate: {
    type: Date,
    default: Date.now
  },
  parentId: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'Folder' // Reference to the Folder model
  }
});

const File = mongoose.model('File', fileSchema);

module.exports = File;
